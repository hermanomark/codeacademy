// created a variable equal to my age
const myAge = 22;
// created a variable earlyYears, use let because value can be change
let earlyYears = 2;
// calculate the dog years for early years
earlyYears *= 10.5;
// set the value for later years
let laterYears = myAge - 2;
// calculate the dog years for later years
laterYears *= 4;
// print early years and later years
console.log(`early years: ${earlyYears}, later years: ${laterYears}`);
// added earlyYears and laterYears
const myAgeInDogYears = earlyYears + laterYears;
// write my name in string and return it as all lower case
myName = "Mark Hermano".toLowerCase();
// write a console.log statement that displays my name and my age in dog years
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`);


